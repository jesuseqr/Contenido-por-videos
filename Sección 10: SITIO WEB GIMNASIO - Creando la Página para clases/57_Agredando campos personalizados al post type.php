1. Descargar, instalar y activar el plugin "Advance custom fields" en WP.

2. Presionar "new" para crear un nuevo grupo de campos. En el campo "Agregar título" colocamos "Clases".

3. En la sección "Location" se define donde se van a mostrar los campos. Solo modificar la opción de la tercera lisrta por "Clase".

4. Presionar "Add field " en la primera sección. Generá diferentes opciones a rellenar, tales como la etiqueta del campo, nombre del campo, tipo de campo. Agregar, desde el primero y consecutivamente: Hora de inicio. hora_inicio. Time Picker. Agrega la hora de inicio de clase. Yes.

El resto se deja como viene por defecto.

5. Para agregar la hora de fin se duplica el orden creado anteriormente. Se modifican los campos desde el primero con: Hora fin. hora_fin. Agregar la hora de fin de clase.

6. Agregar un tercer campo con: Días clase. días_clase. Tex. Escriba los días en que se imparte esta clase. Yes.

Presionar "Publicar" para guardar los datos.

7. Al presionar la opción del menú del dashboard y elegir "Crear clase", se ven 3 campos nuevos al final.
